export function generateSiteConfig(slug: string, payload: IntakeData): string {
  const manufacturers = [
    ...payload.featuredManufacturers,
    ...payload.manufacturersCarried.filter(m => !payload.featuredManufacturers.includes(m)),
  ]

  const locations = payload.locations.length > 0
    ? payload.locations.map(loc => ({
        name: loc.name || payload.businessName,
        address: loc.address,
        city: loc.city,
        state: loc.state,
        zip: loc.zip,
        phone: loc.phone,
        phoneTel: formatPhoneTel(loc.phone),
      }))
    : [{
        name: payload.businessName,
        address: '',
        city: payload.serviceArea,
        state: '',
        zip: '',
        phone: payload.primaryPhone,
        phoneTel: formatPhoneTel(payload.primaryPhone),
      }]

  const teamMembers = payload.teamMembers.map(m => ({
    name: m.name,
    title: m.title,
    credentials: m.credentials,
  }))

  const metaDesc = truncate(
    `${payload.businessName} provides ${payload.primaryServices || 'comprehensive hearing care'} in ${payload.serviceArea || 'your area'}. Schedule your appointment today.`,
    160
  )

  const tagline = payload.serviceArea
    ? `Expert ${payload.practiceType || 'Hearing Care'} in ${payload.serviceArea}`
    : `Expert ${payload.practiceType || 'Hearing Care'}`

  const config = {
    business: {
      name: payload.businessName,
      slug,
      tagline,
      foundedYear: null,
      yearsInBusiness: '',
      reviewCount: '100+',
      reviewRating: '5.0',
    },
    contact: {
      primaryPhone: payload.primaryPhone,
      primaryPhoneTel: formatPhoneTel(payload.primaryPhone),
      primaryEmail: payload.domain ? `info@${payload.domain}` : '',
      bookingUrl: deriveBookingUrl(payload),
      bookingCtaLabel: deriveCtaLabel(payload),
    },
    seo: {
      siteUrl: payload.domain ? `https://${payload.domain}` : '',
      defaultMetaDescription: metaDesc,
    },
    locations,
    manufacturers,
    teamMembers,
    insurancePartners: [],
    nav: [
      { label: 'Hearing Aids', href: '/hearing-aids' },
      { label: 'Hearing Tests', href: '/hearing-tests' },
      { label: 'Our Team', href: '/our-team' },
      { label: 'Services', href: '/services' },
      { label: 'Contact', href: '/contact' },
    ],
  }

  return `// Auto-generated by ELM Pipeline Stage 2
// Client: ${esc(payload.businessName)}
// Generated: ${new Date().toISOString()}

const siteConfig = ${JSON.stringify(config, null, 2)} as const

export default siteConfig
`
}

