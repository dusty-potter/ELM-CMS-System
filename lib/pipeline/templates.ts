// Escape a string for use inside a JS template literal
function esc(s: string): string {
  return s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$/g, '\\$')
}

// Truncate to max length at a word boundary
function truncate(s: string, max: number): string {
  if (s.length <= max) return s
  const cut = s.slice(0, max)
  const last = cut.lastIndexOf(' ')
  return (last > 0 ? cut.slice(0, last) : cut).replace(/[.,;:!?-]+$/, '')
}

function deriveCtaLabel(payload: IntakeData): string {
  if (payload.phoneReliability === 'Strong') return 'Call Now'
  if (payload.schedulingMethod === 'Embedded booking widget') return 'Schedule Appointment'
  if (payload.schedulingMethod === 'External booking URL') return 'Schedule Appointment'
  return 'Request Appointment'
}

function deriveBookingUrl(payload: IntakeData): string {
  if (payload.schedulingMethod === 'Contact form only') return '/contact'
  return '/book'
}

function formatPhoneTel(phone: string): string {
  const digits = phone.replace(/\D/g, '')
  if (digits.length === 10) return `tel:+1${digits}`
  if (digits.length === 11 && digits[0] === '1') return `tel:+${digits}`
  return digits ? `tel:${digits}` : ''
}

