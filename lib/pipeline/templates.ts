// 3. src/config/redirects.ts
// ---------------------------------------------------------------------------

export function generateRedirects(payload: IntakeData): string {
  // Best-effort: extract redirect-looking lines from contentToKeep
  const redirects: Record<string, string> = {}

  if (payload.contentToKeep) {
    const lines = payload.contentToKeep.split('\n')
    for (const line of lines) {
      // Match patterns like "/old-path -> /new-path" or "/old-path => /new-path"
      const match = line.trim().match(/^(\/\S+)\s*(?:->|=>|→)\s*(\/\S+)/)
      if (match) {
        redirects[match[1]] = match[2]
      }
    }
  }

  if (Object.keys(redirects).length === 0) {
    return `// Auto-generated by ELM Pipeline Stage 2
// No redirects extracted from intake. Add manually if needed.

export const REDIRECTS: Record<string, string> = {}
`
  }

  return `// Auto-generated by ELM Pipeline Stage 2

export const REDIRECTS: Record<string, string> = ${JSON.stringify(redirects, null, 2)}
`
}

